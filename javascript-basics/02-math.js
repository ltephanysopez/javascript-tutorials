// JavaScript uses the standard arithmetic operators:
// + (addition)
// - (subtraction)
// * (multiplication)
// / (division)
// % (modulo)

// Additionally, JavaScript has a built-in library for advanced functions

var x = 2;
var y = 1.1;

// console.log() is a function in JavaScript which is used to print any
// kind of variables defined or to print a message to the JavaScript console
console.log(x + y);
console.log(x * y);
console.log((x + y)*(x - y));

var z = 9;
console.log(Math.sqrt(z));
console.log(Math.abs(y/x));
