// Adding Elements to an Array: there are two mutator functions for adding elements to an array


// The push() function adds an element to the end of an array
var nums = [1, 2, 3, 4, 5];
console.log(nums); // 1, 2, 3, 4, 5
nums.push(6);
console.log(nums); // 1, 2, 3, 4, 5, 6

